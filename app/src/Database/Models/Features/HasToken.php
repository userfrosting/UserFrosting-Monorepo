namespace UserFrosting\Sprinkle\Account\Database\Models\Features;

trait HasToken
{
    /**
     * @var string Stores the raw (unhashed) token when created. NOT persisted (saved to db).
     */
    protected $token;

    /**
     * {@inheritDoc}
     */
    public function getToken(): string
    {
        return $this->token;
    }

    /**
     * {@inheritDoc}
     */
    public function setToken(string $value): static
    {
        $this->token = $value;

        return $this;
    }
}
